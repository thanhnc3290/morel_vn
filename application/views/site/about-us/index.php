    /*jquery and jqueryUI (for EASING Bounce effect) required*/

    jQuery(function() { //DOM ready

        /* if div with class="aboutslide_details" is visible in actual viewport,
        with bounce effect */

        jQuery('.aboutslide_details').each(function(i) {

            /*change position of each aboutslide_details*/
            jQuery(this).css("bottom", "50px");

        });

        /* scrolling to bottom: if visible in viewport, fade div in with delay */
        jQuery(window).scroll(function() {

            jQuery('.aboutslide_details').each(function(i) {

                var scrollTop = jQuery(window).scrollTop();
                var height = jQuery(window).height();
                var scrollBottom = scrollTop + height + jQuery(this).height();
                if (this.offsetParent != null) {
                    var obj = jQuery(this.offsetParent).position().top * 1.2 + jQuery(this)
                        .outerHeight();
                    if (scrollBottom > obj) {
                        jQuery('.animation-bounce').each(function() {
                            this.classList.remove('animation-bounce');
                        })
                        setTimeout(() => {
                            this.classList.add('animation-bounce');
                        }, 1000);

                    } else {
                        this.classList.remove('animation-bounce');
                    }
                }
            });

        });

    });
